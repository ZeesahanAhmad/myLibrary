    private void b_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_tableMouseClicked
        // code to select row in book search table:
        int sr=b_table.getSelectedRow();
        int sno=(int) b_table.getValueAt(sr, 0);
        String nm=(String) b_table.getValueAt(sr, 1);
        String pb=(String) b_table.getValueAt(sr, 2);
        String au=(String) b_table.getValueAt(sr, 3);
        int qt=(int) b_table.getValueAt(sr, 4);
        
        sn.setText(sno+"");
        n.setText(nm);
        p.setText(pb);
        a.setText(au);
        q.setText(qt+"");


    }//GEN-LAST:event_b_tableMouseClicked

