        String s3=auth.getText();
        int s4=Integer.parseInt(qty.getText());
        try{
            database.database_connection.b_register.setString(1, s1);
            database.database_connection.b_register.setString(2, s2);
            database.database_connection.b_register.setString(3, s3);
            database.database_connection.b_register.setInt(4, s4);
            database.database_connection.b_register.executeUpdate();
            JOptionPane.showMessageDialog(null, "Book register successfully");



            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }
        
        
